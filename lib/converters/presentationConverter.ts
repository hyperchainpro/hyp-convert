// EXTRACTION: PPTX → Text
// =========================================

/**
 * Extract meaningful text from PPTX
 * Parses slide XML to find <a:t> text elements in correct order
 */
export const pptxToText = async (uri: string): Promise<{ content?: string; success: boolean; error?: string }> => {
    try {
        // Load ZIP
        let zipData: any;
        if (Platform.OS === 'web') {
            const response = await fetch(uri);
            zipData = await response.arrayBuffer();
        } else {
            zipData = await FileUtils.readAsBase64Async(uri);
        }

        const loadOptions = Platform.OS === 'web' ? {} : { base64: true };
        const zip = await JSZip.loadAsync(zipData, loadOptions);

        // Find all slide files (ppt/slides/slide1.xml, slide2.xml, etc.)
        const slideFiles = Object.keys(zip.files)
            .filter(f => /^ppt\/slides\/slide\d+\.xml$/i.test(f))
            .sort((a, b) => {
                const numA = parseInt(a.match(/slide(\d+)/)?.[1] || '0');
                const numB = parseInt(b.match(/slide(\d+)/)?.[1] || '0');
                return numA - numB;
            });

        if (slideFiles.length === 0) {
            return { success: false, error: 'Tidak ada slide ditemukan di file PPTX' };
        }

        let fullText = '';

        for (let i = 0; i < slideFiles.length; i++) {
            const slideFile = zip.file(slideFiles[i]);
            if (!slideFile) continue;

            const xmlContent = await slideFile.async('string');

            // Extract all <a:t> text elements (PowerPoint text tags)
            const textElements: string[] = [];
            const textRegex = /<a:t>([^<]*)<\/a:t>/g;
            let match;
            while ((match = textRegex.exec(xmlContent)) !== null) {
                if (match[1].trim()) {
                    textElements.push(match[1]);
                }
            }

            if (textElements.length > 0) {
                fullText += `\n--- Slide ${i + 1} ---\n`;

                // Group text elements - detect paragraph breaks from <a:p> tags
                const paragraphTexts: string[] = [];
                let currentParagraph = '';
                const paragraphRegex = /<a:p>([\s\S]*?)<\/a:p>/g;
                let pMatch;

                while ((pMatch = paragraphRegex.exec(xmlContent)) !== null) {
                    const paragraphXml = pMatch[1];
                    const innerTexts: string[] = [];
                    const innerTextRegex = /<a:t>([^<]*)<\/a:t>/g;
                    let tMatch;
                    while ((tMatch = innerTextRegex.exec(paragraphXml)) !== null) {
                        innerTexts.push(tMatch[1]);
                    }
                    if (innerTexts.length > 0) {
                        paragraphTexts.push(innerTexts.join(''));
                    }
                }

                if (paragraphTexts.length > 0) {
                    fullText += paragraphTexts.join('\n') + '\n';
                } else {
                    fullText += textElements.join(' ') + '\n';
                }
            }
        }

        if (!fullText.trim()) {
            return { success: true, content: '[Presentasi ini tidak mengandung teks yang dapat diekstrak.]' };
        }

        return { content: fullText.trim(), success: true };
    } catch (error) {
        return { success: false, error: error instanceof Error ? error.message : 'Gagal membaca PPTX' };
    }
};

