/**
 * Convert Markdown to PPTX
 * Uses # headings as slide breaks
 */
export const markdownToPPTX = async (markdown: string, title: string = 'Presentation'): Promise<{ uri?: string; success: boolean; error?: string }> => {
    try {
        // Split markdown on headings to create slides
        const slides: { heading: string; content: string }[] = [];
        const lines = markdown.split('\n');
        let currentHeading = title;
        let currentContent: string[] = [];

        for (const line of lines) {
            const headingMatch = line.match(/^(#{1,3})\s+(.+)/);
            if (headingMatch) {
                // Save previous slide
                if (currentContent.length > 0 || slides.length === 0) {
                    slides.push({ heading: currentHeading, content: currentContent.join('\n') });
                }
                currentHeading = headingMatch[2];
                currentContent = [];
            } else {
                currentContent.push(line);
            }
        }

        // Don't forget the last slide
        if (currentContent.length > 0) {
            slides.push({ heading: currentHeading, content: currentContent.join('\n') });
        }

        // Build text for the generic textToPPTX
        const text = slides.map(s => `${s.heading}\n${s.content}`).join('\n\n');

        return await textToPPTX(text, title);
    } catch (error) {
        return { success: false, error: error instanceof Error ? error.message : 'Gagal mengkonversi Markdown ke PPTX' };
    }
};
